package livroWebDesenvolvimento;

public class ExercicioLivro07While {
    public static void main(String[] args) {
        int intContador;
        intContador = 1;
        while(intContador < 100){
            System.out.println(intContador +" X 7 = " +(intContador*7) );

            intContador++;
        }
    }
}
